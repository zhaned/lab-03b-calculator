export function addition(firstInput, secondInput) {
    const result = firstInput + secondInput;
    return result;
}

export function subtraction(firstInput, secondInput) {
    const result = firstInput - secondInput;
    return result;
}

export function multiply(firstInput, secondInput) {
    const result = firstInput * secondInput;
    return result;
}

export function division(firstInput, secondInput) {
    const result = firstInput / secondInput;
    return result;
}
